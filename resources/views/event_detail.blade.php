            x = document.getElementsByClassName('contain');

            document.getElementById('number').innerHTML = 1 + ' / ' + x.length;
            function next() {
                for (var index = 0; index < x.length; index++) {
                    const element = x[index];
                    // console.log(element);
                    if (element.classList.length == 2) {
                        element.classList.add('d-none');
                        break

                    }

                }
                n = index + 1;


                if (n > x.length - 1) {
                    n = 0;
